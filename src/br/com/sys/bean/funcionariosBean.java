package br.com.sys.bean;

public class funcionariosBean {
	private int idFuncionario;
	private String nomeFuncionario;
	private String cpfFuncionario;
	private String telefoneFuncionario;
	private int idEndereco;
	private int idNivel_Acesso;
	public int getIdFuncionario() {
		return idFuncionario;
	}
	public void setIdFuncionario(int idFuncionario) {
		this.idFuncionario = idFuncionario;
	}
	public String getNomeFuncionario() {
		return nomeFuncionario;
	}
	public void setNomeFuncionario(String nomeFuncionario) {
		this.nomeFuncionario = nomeFuncionario;
	}
	public String getCpfFuncionario() {
		return cpfFuncionario;
	}
	public void setCpfFuncionario(String cpfFuncionario) {
		this.cpfFuncionario = cpfFuncionario;
	}
	public String getTelefoneFuncionario() {
		return telefoneFuncionario;
	}
	public void setTelefoneFuncionario(String telefoneFuncionario) {
		this.telefoneFuncionario = telefoneFuncionario;
	}
	public int getIdEndereco() {
		return idEndereco;
	}
	public void setIdEndereco(int idEndereco) {
		this.idEndereco = idEndereco;
	}
	public int getIdNivel_Acesso() {
		return idNivel_Acesso;
	}
	public void setIdNivel_Acesso(int idNivel_Acesso) {
		this.idNivel_Acesso = idNivel_Acesso;
	}
	

}
