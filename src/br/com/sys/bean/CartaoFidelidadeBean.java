package br.com.sys.bean;

public class cartaoFidelidadeBean {
	private int idCartao;
	private int idCliente;
	private int metaFidelidade;
	private int relizadoFidelidade;
	public int getIdCartao() {
		return idCartao;
	}
	public void setIdCartao(int idCartao) {
		this.idCartao = idCartao;
	}
	public int getIdCliente() {
		return idCliente;
	}
	public void setIdCliente(int idCliente) {
		this.idCliente = idCliente;
	}
	public int getMetaFidelidade() {
		return metaFidelidade;
	}
	public void setMetaFidelidade(int metaFidelidade) {
		this.metaFidelidade = metaFidelidade;
	}
	public int getRelizadoFidelidade() {
		return relizadoFidelidade;
	}
	public void setRelizadoFidelidade(int relizadoFidelidade) {
		this.relizadoFidelidade = relizadoFidelidade;
	}

}
